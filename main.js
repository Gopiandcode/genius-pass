function trySaveNewPassword() {
  // keep on generating passwords until one with more than 0 results
  var password = generatePassword();
  $.ajax(
    {
      url: "https://api.pwnedpasswords.com/pwnedpassword/" + password
    },
    function(data) {
      message("Generated password of security: " + JSON.stringify(data));
    }
  )
    .done(function() {
      var store = {};
      store[location.href] = password;
      chrome.storage.sync.set(store, function(values) {
        $("input:password").val(password);
        chrome.runtime.sendMessage({url: location.href, password: password});
      });
    })
    .fail(function(data) {
      // loop until found new password
      trySaveNewPassword();
    });
}

